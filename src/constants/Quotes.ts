// Biến cục bộ để ghi nhớ câu vừa hiển thị (Tránh lặp)
let lastQuote = "";

export const getRandomQuote = (mode: string): string => {
  const key = mode.toUpperCase() as keyof typeof QUOTES_DATA;
  const list = QUOTES_DATA[key] || QUOTES_DATA.WORK;
  
  // Nếu mảng chỉ có 1 câu thì trả về luôn
  if (list.length <= 1) return list[0];

  let newQuote: string;
  do {
    // Bốc ngẫu nhiên một câu
    newQuote = list[Math.floor(Math.random() * list.length)];
  } while (newQuote === lastQuote); // Nếu trùng câu cũ thì bốc lại

  lastQuote = newQuote; // Lưu lại để lần sau kiểm tra
  return newQuote;
};